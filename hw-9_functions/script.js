function getInfo(array, caption) {
	document.write(`<table><caption>${caption} info</caption><tbody>`);
	for (i=0; i <array.length; i++) {
		document.write(`
				<tr>`);
			for(j=0; j<array[i].length; j++) {
				if (Array.isArray(array[i][j]))	{
					str = array[i][j].join('; ');
					document.write(`
				<td>${str}</td>`);
				} else {
					document.write(`
					<td>${array[i][j]}</td>`);
				}
				
			}
			document.write(`</tr>`);
	}
	document.write(`</tbody></table>
	<br>`);
}

